# str()
# str() - is used for converting all types of values into str
# syntax: varname=str(int/float/bool/complex)

#int to str type --> possible

a =  12
b = str(a)
print(b,type(b))


#float to str type
a =  12.65
b = str(a)
print(b,type(b))


#bool to str type
a =  True
b = str(a)
print(b,type(b))


#complex to str type
a =  12+76j
b = str(a)
print(b,type(b))
